def main():
    data = load_dashboard()

    # ── Sidebar ──────────────────────────────────────────────────────────────
    with st.sidebar:
        st.image("https://upload.wikimedia.org/wikipedia/commons/thumb/b/b8/Lufthansa_Logo_2018.svg/320px-Lufthansa_Logo_2018.svg.png",
                 width=160)
        st.markdown("## AI CEO Dashboard")
        st.markdown("Strategic Intelligence Agent")
        st.markdown("---")

        if data:
            built_at = data.get("built_at", "")
            if built_at:
                try:
                    ts = datetime.fromisoformat(built_at).strftime("%d %b %Y %H:%M")
                except Exception:
                    ts = built_at
                st.caption(f"Last built: {ts}")
        else:
            st.warning("No dashboard data found.\nRun `dashboard_builder.py` first.")

        st.markdown("---")
        nav = st.radio(
            "Navigate to",
            options=[
                "🏢 Company Overview",
                "📰 Market Intelligence",
                "🚀 Opportunity Monitor",
                "⚠️  Risk Monitor",
                "💬 Sentiment Analysis",
                "🎯 Strategic Recommendations",
                "👔 CEO Briefing",
            ],
        )

        st.markdown("---")
        if st.button("🔄 Refresh Data"):
            st.cache_data.clear()
            st.rerun()

    # ── Main content ─────────────────────────────────────────────────────────
    if not data:
        st.error("Dashboard data not found. Please run `python dashboard_builder.py` first.")
        st.code("python dashboard_builder.py")
        return

    section_map = {
        "🏢 Company Overview":          ("section_1", render_section_1),
        "📰 Market Intelligence":       ("section_2", render_section_2),
        "🚀 Opportunity Monitor":       ("section_3", render_section_3),
        "⚠️  Risk Monitor":             ("section_4", render_section_4),
        "💬 Sentiment Analysis":        ("section_5", render_section_5),
        "🎯 Strategic Recommendations": ("section_6", render_section_6),
        "👔 CEO Briefing":              ("section_7", render_section_7),
    }

    sid, renderer = section_map[nav]
    section_data  = get_section(data, sid)

    # Section 7 is a flat dict, not a section wrapper
    if sid == "section_7":
        renderer(section_data)
    else:
        status = section_data.get("status", "")
        if status == "partial":
            skipped = section_data.get("skipped_ids", [])
            st.warning(f"Partial data — {len(skipped)} query file(s) not yet available: {skipped}")
        renderer(section_data)


if __name__ == "__main__":
    main()
