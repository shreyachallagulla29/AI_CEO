def render_section_5(section: dict):
    st.markdown('<div class="section-header">💬 Section 5 — Sentiment Analysis</div>', unsafe_allow_html=True)

    if not entries(section):
        st.info("No sentiment entries available yet.")
        return

    for e in entries(section):
        if "error" in e or e.get("status") == "structuring_failed":
            st.warning(f"[{e.get('query_id')}] Structuring failed: {e.get('error','')[:200]}")
            continue

        news_label   = e.get("news_sentiment",   "neutral")
        pub_label    = e.get("public_sentiment",  "neutral")
        news_score   = float(e.get("news_sentiment_score",  0) or 0)
        pub_score    = float(e.get("public_sentiment_score", 0) or 0)
        trends       = e.get("sentiment_trends") or []
        themes       = e.get("key_themes")       or []
        summary      = e.get("sentiment_summary", "")
        company      = e.get("company", "")

        st.markdown(f'<span class="badge badge-purple">{company}</span>', unsafe_allow_html=True)
        if summary:
            st.markdown(f"> {summary}")

        # Gauge charts
        col1, col2 = st.columns(2)

        def gauge(score: float, title: str) -> go.Figure:
            # score is -1 to 1; map to 0–100 for gauge
            val = round((score + 1) / 2 * 100, 1)
            color = "#38a169" if val >= 60 else ("#d69e2e" if val >= 40 else "#e53e3e")
            fig = go.Figure(go.Indicator(
                mode="gauge+number",
                value=val,
                title={"text": title, "font": {"size": 14}},
                gauge={
                    "axis": {"range": [0, 100]},
                    "bar": {"color": color},
                    "steps": [
                        {"range": [0,  40], "color": "#fee2e2"},
                        {"range": [40, 60], "color": "#fef3c7"},
                        {"range": [60, 100], "color": "#d1fae5"},
                    ],
                    "threshold": {"line": {"color": "#1a202c", "width": 2}, "value": val},
                },
                number={"suffix": "%"},
            ))
            fig.update_layout(height=220, margin=dict(t=40, b=10, l=20, r=20))
            return fig

        with col1:
            st.plotly_chart(gauge(news_score, f"News Sentiment ({news_label.capitalize()})"),
                            use_container_width=True)
        with col2:
            st.plotly_chart(gauge(pub_score, f"Public Sentiment ({pub_label.capitalize()})"),
                            use_container_width=True)

        col3, col4 = st.columns(2)
        with col3:
            if trends:
                st.markdown("**📈 Sentiment Trends**")
                for t in trends:
                    st.markdown(f"- {t}")
        with col4:
            if themes:
                st.markdown("**🏷️ Key Themes**")
                for th in themes:
                    st.markdown(f"- {th}")

        st.markdown("---")


