def render_section_1(section: dict):
    st.markdown('<div class="section-header">🏢 Section 1 — Company Overview</div>', unsafe_allow_html=True)
    d = section.get("data", {})

    col1, col2, col3, col4 = st.columns(4)
    col1.metric("Company",          d.get("company_name", "—"))
    col2.metric("Industry",         d.get("industry", "—"))
    col3.metric("Collected Documents", d.get("num_collected_documents", "—"))
    col4.metric("Data Sources",     d.get("num_data_sources", "—"))

    st.markdown("---")
    c1, c2 = st.columns(2)

    with c1:
        st.markdown("**📡 Data Sources**")
        for src in d.get("data_sources", []):
            st.markdown(f"- {src}")

    with c2:
        st.markdown("**🏁 Competitors Monitored**")
        for comp in d.get("competitors_monitored", []):
            st.markdown(f"- {comp}")

    ts = d.get("last_updated", section.get("generated_at", ""))
    if ts:
        try:
            ts_fmt = datetime.fromisoformat(ts).strftime("%d %b %Y, %H:%M UTC")
        except Exception:
            ts_fmt = ts
        st.caption(f"Last updated: {ts_fmt}")


