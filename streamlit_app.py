@st.cache_data(ttl=60)
def load_dashboard() -> dict:
    """Load dashboard_payload.json. Returns {} if not found."""
    if DASHBOARD_PAYLOAD.exists():
        with open(DASHBOARD_PAYLOAD, encoding="utf-8") as f:
            return json.load(f)
    # Try loading individual section files
    sections = {}
    for p in sorted(SECTION_FILES.glob("section_*.json")):
        sid = p.stem
        with open(p, encoding="utf-8") as f:
            sections[sid] = json.load(f)
    if sections:
        return {"sections": sections, "built_at": "from individual files"}
    return {}


def get_section(data: dict, section_id: str) -> dict:
    return data.get("sections", {}).get(section_id, {})


def entries(section: dict) -> list:
    return section.get("entries", [])


# =============================================================================
# BADGE HELPERS
# =============================================================================

def severity_badge(level: str) -> str:
    level = (level or "").lower()
    cls   = {"high": "badge-high", "medium": "badge-medium", "low": "badge-low"}.get(level, "badge-blue")
    return f'<span class="badge {cls}">{level.upper()}</span>'


def sentiment_badge(label: str) -> str:
    label = (label or "").lower()
    cls   = {"positive": "badge-positive", "neutral": "badge-neutral", "negative": "badge-negative"}.get(label, "badge-blue")
    return f'<span class="badge {cls}">{label.upper()}</span>'


def category_badge(label: str) -> str:
    return f'<span class="badge badge-blue">{(label or "").replace("_", " ").upper()}</span>'


def priority_badge(level: str) -> str:
    return severity_badge(level)


